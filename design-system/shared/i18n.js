// Helper for language toggle — sets persisted lang and dispatches event.
window.VL_lang = function(){
  const key = "vl-lang";
  return {
    get(){ return localStorage.getItem(key) || "en"; },
    set(v){
      localStorage.setItem(key, v);
      document.documentElement.lang = v;
      window.dispatchEvent(new CustomEvent("vl-lang-change", { detail: v }));
    }
  };
}();
